    private void Almacenar() {
        NegocioProducto neg = new NegocioProducto();
        if(neg.existeNombre(this.txtNombre.getText().trim()) == true)
        {
            JOptionPane.showMessageDialog(this,
                "Nombre Existe",
                "Error de Ingreso",
                JOptionPane.ERROR_MESSAGE);
            this.txtNombre.requestFocus();
            return;
        }
        Producto acad = new Producto();
        acad.setNombre(this.txtNombre.getText().trim());
        acad.setCantidad(this.txtCantidad.getText().trim());
        neg.ingresarProducto(acad);
    }
