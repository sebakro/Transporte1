    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        //Verificar si existe el Rut
        NegocioProducto neg = new NegocioProducto();
        if(neg.existeNombre(this.txtNombre.getText().trim()) == false)
        {
            JOptionPane.showMessageDialog(this, 
                        "Nombre no existe",
                        "Error de Ingreso", 
                        JOptionPane.ERROR_MESSAGE);
            this.txtNombre.requestFocus();
            return;
        }
        
        Producto acad = neg.buscarProducto(this.txtNombre.getText().trim());
        this.txtNombre.setText(acad.getNombre());
        this.txtCantidad.setText(acad.getNombre());
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(this,"¿Desea eliminar el Producto?", 
                "Confirmar Eliminación", JOptionPane.YES_NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE);
        if(opcion == JOptionPane.YES_OPTION)
        {
            NegocioProducto neg = new NegocioProducto();
            neg.eliminarProducto(this.txtNombre.getText().trim());            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

